#include <iostream>
using namespace std;

int main() 
{
    int choice;
    cout << "Welcome to Fibonacci Solitaire!" << endl;
    cout << "1. New Deck" << endl;
    cout << "2. Display Deck" << endl;
    cout << "3. Shuffle Deck" << endl;
    cout << "4. Play Solitaire" << endl;
    cout << "5. Exit" << endl;
    cin >>  choice;
    if (choice == 1)
    {
        cout << "a";
    }
    if (choice == 2)
    {
        cout << "b";
    }
    if (choice == 3)
    {
        cout << "c";
    }
    if (choice == 4)
    {
        cout << "d";
    }
    if (choice == 5)
    {
        cout << "e";
    }
    return 0;
}
